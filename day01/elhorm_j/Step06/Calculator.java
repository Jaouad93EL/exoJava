public class Calculator {

    public double calculate(double d1 , char c , double d2) {
	
	if (c == '+')
	    return d1 + d2;
	else if (c == '-')
	    return d1 - d2;
	else if (c == '/' && d2 != 0)
	    return d1 / d2;
	else if (c == '*')
	    return d1 * d2;
	System.out.println("ERROR");
	return -1;
    }
}
