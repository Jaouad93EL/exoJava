import java.util.*;

public class Rakuten implements MarketPlace {

    private List<String> markets;

    public Rakuten() {
	this.markets = new ArrayList<String>();
    }
    
    public List<String> getMarketsNames() {
	return this.markets;
    }

    public void addMarket(String name) {
	this.markets.add(name);
    }
}
